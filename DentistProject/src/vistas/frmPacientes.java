    private void rbtFrecuenteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rbtFrecuenteActionPerformed
        HClinico h = new daoHClinico().BuscaHCP(Integer.parseInt(txtIdPaciente.getText()));
        frmHClinico f;
        if (h != null) {
            f = new frmHClinico(new daoHClinico().BuscaHCP(Integer.parseInt(txtIdPaciente.getText())));
        } else {
            f = new frmHClinico();
        }
        catalogoPacientes.getFrmPrincipal().getDskPanel().add(f);
        f.show();
    }//GEN-LAST:event_rbtFrecuenteActionPerformed


