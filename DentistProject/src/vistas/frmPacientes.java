    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        try {

            Pacientes paciente = new Pacientes(Integer.parseInt(txtIdPaciente.getText()),
                    txtNombre.getText(), txtApellidoPaterno.getText(), txtApellidoMaterno.getText(),
                    Integer.parseInt(txtEdad.getText()), txtDireccion.getText(), cmbSexo.getSelectedItem().toString(), cmbEstadoCivil.getSelectedItem().toString(),
                    txtDiagnostico.getText(), txtPadecimiento.getText(), texTratamiento.getText(), texActividades.getText(),
                    Integer.parseInt(txtDia.getText()), Integer.parseInt(txtMes.getText()), Integer.parseInt(txtYear.getText()), rbtFrecuente.isSelected());
            if (btnAceptar.getText().equals("Guardar")) {
                boolean s = new daoPacientes().Insert(paciente);
                if (s) {
                    JOptionPane.showMessageDialog(rootPane, "Paciente Agregado");
                    this.dispose();
                }

            } else {
                boolean s = new daoPacientes().Update(paciente);
                if (s) {
                    JOptionPane.showMessageDialog(rootPane, "Paciente Actualizado");
                    this.dispose();
                }

            }
            if (catalogoPacientes != null) {
                catalogoPacientes.tabla();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,
                    "Ha ocurrido un error al leer los datos, algunos no cumplen con el formato correcto",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnAceptarActionPerformed

